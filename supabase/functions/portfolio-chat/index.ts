import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are an AI assistant for Joderick Sherwin J's portfolio. You have deep knowledge about his projects and experience:

PROJECTS:
1. TerraDefender - Military IPB System: Deep Learning-based system for terrain analysis from aerial/satellite imagery using TensorFlow, Keras, OpenCV, Flask
2. Analyst Recommendation System: AI-driven system matching analysts with projects using Scikit-learn, Pandas
3. Animoji Simulation: Real-time facial tracking system using OpenCV, MediaPipe
4. Project WraithCast: Real-time wireframe camera system for 3D skeletal representations
5. RAG Project: Retrieval Augmented Generation system combining document retrieval with generative AI
6. Mental Health Chatbot: Compassionate AI using NLP for emotional support
7. Speech to Text Converter: PyQt5-based transcription application

EXPERTISE: Machine Learning, Deep Learning, Computer Vision, NLP, Python, TensorFlow, Keras, Scikit-learn, OpenCV, Flask

Be conversational, enthusiastic, and knowledgeable. Answer questions about the projects, technologies, and experience. Keep responses concise (2-3 sentences) unless more detail is requested.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limits exceeded, please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Payment required, please add funds to your Lovable AI workspace." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
